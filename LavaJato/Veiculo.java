
package Lavajato;

public class Veiculo {
    private String cor;
    private String placa;

    public Veiculo(String cor,String placa) {
        this.cor= cor;
        this.placa= placa;
    }

    public String getCor() {
        return cor;
    }

    public String getPlaca() {
        return placa;
    }
}
